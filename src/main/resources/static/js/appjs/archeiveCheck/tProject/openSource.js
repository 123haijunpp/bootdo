var prefix = "/archiveCheck/tProject"
var state = "1";
$(function () {
    load(state);
});


function reLoad() {
    $('#exampleTable').bootstrapTable('refresh');
}

/**
 *  已归档
 */
function archived() {
    $("#exampleTable").bootstrapTable('destroy');
    load(state);
}

/**
 * 已开源
 */
function open_source() {
    state = "2";
    // 在初始化table之前，要将table销毁，否则会保留上次加载的内容
    $("#exampleTable").bootstrapTable('destroy');
    load(state);
}

function remove(id) {
    layer.confirm('确定要删除选中的记录？', {
        btn: ['确定', '取消']
    }, function () {
        $.ajax({
            url: prefix + "/remove",
            type: "post",
            data: {
                'id': id
            },
            success: function (r) {
                if (r.code == 0) {
                    layer.msg(r.msg);
                    reLoad();
                } else {
                    layer.msg(r.msg);
                }
            }
        });
    })
}

